/**
 * `WebhookEndpointStore` ‚Äî persistence for `webhook_endpoints` (migration
 * 022, `src/db/migrate.ts`; HT-68, specs/plugins/substrate-v1.md ¬ß5 ‚Äî
 * "module" below always means an out-of-process Helpthread extension,
 * never the legal "plugin exception" phrase CHARTER.md ¬ß7 uses).
 *
 * Mirrors `src/store/mailbox-tokens.ts`'s encrypt-at-rest shape: the
 * caller supplies a PLAINTEXT secret, this store encrypts it with
 * `src/store/token-crypto.ts`'s existing AES-256-GCM envelope before it
 * ever reaches the database, and only {@link WebhookEndpointStore.getSecret}
 * decrypts it back ‚Äî the one caller with a legitimate need (wave 3's
 * delivery handler, signing `X-Helpthread-Signature`, spec ¬ß5: "signing
 * needs the plaintext back"). No new crypto code ‚Äî a second caller of the
 * module `mailbox-tokens.ts` already depends on.
 *
 * ## Secret generation is NOT this module's job
 *
 * Spec ¬ß5 says the secret is "server-generated" ‚Äî but exactly like
 * `src/store/assistants.ts`'s token-hashing boundary, generating it is
 * wave 2/3 admin-route work (`POST /api/v1/webhooks`) this ticket's
 * boundary excludes. {@link WebhookEndpointStore.create} takes an
 * already-generated plaintext `secret` and only handles encrypting it at
 * rest.
 *
 * ## Auto-disable (spec ¬ß5, spec ¬ß9 decision 2: 20, admin re-enable)
 *
 * {@link WebhookEndpointStore.recordDeliveryFailure} increments
 * `consecutive_failures` and, ONLY when the endpoint is currently `'active'`
 * (never overriding an operator's deliberate `'disabled'`), flips it to
 * `'auto_disabled'` the moment the counter reaches {@link
 * WEBHOOK_AUTO_DISABLE_THRESHOLD}. {@link
 * WebhookEndpointStore.recordDeliverySuccess} resets the counter to zero
 * but does NOT touch `status` ‚Äî re-enabling a disabled/auto_disabled
 * endpoint is a deliberate admin action ({@link WebhookEndpointStore.patch}
 * with `status: 'active'`), never an implicit side effect of one stray
 * success. `patch`'s own `status: 'active'` branch resets the counter to
 * zero for the same reason `recordDeliverySuccess` does ‚Äî a fresh
 * re-enable should not inherit a near-threshold failure count from before
 * the operator intervened.
 */

import type { Db, SqlValue } from '../db/client.js'
import { decrypt, encrypt } from './token-crypto.js'

/** A webhook endpoint's lifecycle status (spec ¬ß5). `active` receives deliveries; `disabled` is an operator's own choice; `auto_disabled` is written only by {@link WebhookEndpointStore.recordDeliveryFailure} at the failure threshold ‚Äî kept distinct from `disabled` so an auto-disable can never masquerade as a manual one. */
export type WebhookEndpointStatus = 'active' | 'disabled' | 'auto_disabled'

/** Consecutive delivery failures at which an `active` endpoint auto-disables (spec ¬ß9 decision 2). */
export const WEBHOOK_AUTO_DISABLE_THRESHOLD = 20

/** Input to {@link WebhookEndpointStore.create}. */
export interface NewWebhookEndpoint {
  /** `https://` only ‚Äî enforced by both the schema CHECK (migration 022) and, eventually, wave 3's SSRF-pinning delivery handler (spec ¬ß5). */
  url: string
  /** Plaintext secret ‚Äî server-generated by the caller (wave 2/3), encrypted by this store before persist. Never stored or logged in plaintext. */
  secret: string
  /** A subset of spec ¬ß4's event-type list, or `[]` (interpreted by the API layer as "all" ‚Äî this store takes no position, spec ¬ß5's "or all"). Persisted verbatim, the same `conversations.tags` convention. */
  events: string[]
  /** The slug of the module that registered this endpoint, or omitted/`null` for an operator-registered one not tied to any module (spec ¬ß1's additive-forward rule). */
  module?: string | null
}

/** A webhook endpoint as read back from storage ‚Äî camelCase, timestamps as `Date`. Never carries the secret (see {@link WebhookEndpointStore.getSecret} for the one path that reaches it, decrypted). */
export interface StoredWebhookEndpoint {
  id: string
  url: string
  events: string[]
  module: string | null
  status: WebhookEndpointStatus
  consecutiveFailures: number
  createdAt: Date
  updatedAt: Date
}

/** {@link WebhookEndpointStore.create}'s return shape: the stored fields plus the plaintext `secret`, returned exactly once (spec ¬ß5: "returned once") ‚Äî never re-derivable after this call, since only the ciphertext persists. */
export interface CreatedWebhookEndpoint extends StoredWebhookEndpoint {
  secret: string
}

/** Fields {@link WebhookEndpointStore.patch} may change (spec ¬ß5's admin API: `PATCH /api/v1/webhooks/{id}`). `status` here is deliberately narrower than {@link WebhookEndpointStatus} ‚Äî `'auto_disabled'` is engine-written only (module doc), never admin-settable directly; an admin re-enables by setting `'active'`. */
export interface WebhookEndpointPatch {
  url?: string
  events?: string[]
  module?: string | null
  status?: 'active' | 'disabled'
}

/** Persistence operations for `webhook_endpoints`. See the module doc for the encrypt-at-rest and auto-disable contracts. */
export interface WebhookEndpointStore {
  /** Insert a new endpoint, `status: 'active'` (the schema default), encrypting `input.secret` before persist. Returns the created row WITH the plaintext secret (spec ¬ß5: shown once). */
  create(input: NewWebhookEndpoint): Promise<CreatedWebhookEndpoint>

  /** List every webhook endpoint, ordered by `created_at` ‚Äî the roster `GET /api/v1/webhooks` (spec ¬ß5) serves. Never includes the secret. */
  list(): Promise<StoredWebhookEndpoint[]>

  /** Apply `patch` to endpoint `id`. A `status: 'active'` transition also resets `consecutive_failures` to `0` (module doc). Returns the updated record, or `null` if `id` doesn't exist. */
  patch(id: string, patch: WebhookEndpointPatch): Promise<StoredWebhookEndpoint | null>

  /** Hard delete endpoint `id`. Returns `true` if a row was deleted, `false` if `id` didn't exist. */
  delete(id: string): Promise<boolean>

  /**
   * Decrypt and return endpoint `id`'s secret ‚Äî the one path that reaches
   * plaintext after creation (wave 3's delivery handler, signing the HMAC
   * per spec ¬ß5). `null` if `id` doesn't exist. Throws if decryption fails
   * (see `token-crypto.ts`'s `decrypt`) rather than returning a silently
   * wrong value.
   */
  getSecret(id: string): Promise<string | null>

  /**
   * Record one failed delivery attempt (wave 3's delivery handler, after
   * exhausting the queue's retry policy ‚Äî spec ¬ß5: "dead-letters against
   * the endpoint"): increments `consecutive_failures`, and ‚Äî only if the
   * endpoint is currently `'active'` ‚Äî flips it to `'auto_disabled'` once
   * the counter reaches {@link WEBHOOK_AUTO_DISABLE_THRESHOLD}, in the SAME
   * statement (no separate read-then-write race). Returns the updated
   * record, or `null` if `id` doesn't exist.
   */
  recordDeliveryFailure(id: string): Promise<StoredWebhookEndpoint | null>

  /**
   * Record one successful delivery: resets `consecutive_failures` to `0`.
   * Does NOT change `status` (module doc). Returns the updated record, or
   * `null` if `id` doesn't exist.
   */
  recordDeliverySuccess(id: string): Promise<StoredWebhookEndpoint | null>
}

/** Raw `webhook_endpoints` row shape, before mapping to {@link StoredWebhookEndpoint}. */
interface WebhookEndpointRow {
  id: string
  url: string
  secret_ciphertext: Uint8Array
  events: unknown
  module: string | null
  status: string
  consecutive_failures: number
  created_at: Date | string
  updated_at: Date | string
}

const WEBHOOK_ENDPOINT_COLUMNS =
  'id, url, secret_ciphertext, events, module, status, consecutive_failures, created_at, updated_at'

function toDate(value: Date | string): Date {
  return value instanceof Date ? value : new Date(value)
}

function toStoredWebhookEndpoint(row: WebhookEndpointRow): StoredWebhookEndpoint {
  return {
    id: row.id,
    url: row.url,
    // Cast, not parsed ‚Äî same reasoning as conversations.ts's `tags`: this
    // codebase is the only writer (always a JSON string array), and jsonb
    // arrives already decoded.
    events: row.events as string[],
    module: row.module,
    status: row.status as WebhookEndpointStatus,
    consecutiveFailures: row.consecutive_failures,
    createdAt: toDate(row.created_at),
    updatedAt: toDate(row.updated_at),
  }
}

/**
 * Create a {@link WebhookEndpointStore} backed by `db`, encrypting/
 * decrypting secrets with `encryptionKey`. `encryptionKey` must be a
 * 32-byte `Buffer` ‚Äî decode it once at the composition root via
 * `token-crypto.ts`'s `decodeEncryptionKey` and pass the result in here,
 * the same convention `createMailboxTokenStore` uses (and, in a real
 * deployment, the SAME key ‚Äî this module reuses `mailbox-tokens.ts`'s
 * crypto primitives, not a second key hierarchy).
 */
export function createWebhookEndpointStore(db: Db, encryptionKey: Buffer): WebhookEndpointStore {
  return {
    async create(input) {
      const secretCiphertext = encrypt(input.secret, encryptionKey)
      const [row] = await db.query<WebhookEndpointRow>(
        `INSERT INTO webhook_endpoints (url, secret_ciphertext, events, module)
         VALUES ($1, $2, $3::jsonb, $4)
         RETURNING ${WEBHOOK_ENDPOINT_COLUMNS}`,
        [input.url, secretCiphertext, JSON.stringify(input.events), input.module ?? null],
      )
      return { ...toStoredWebhookEndpoint(row), secret: input.secret }
    },

    async list() {
      const rows = await db.query<WebhookEndpointRow>(
        `SELECT ${WEBHOOK_ENDPOINT_COLUMNS} FROM webhook_endpoints ORDER BY created_at`,
      )
      return rows.map(toStoredWebhookEndpoint)
    },

    async patch(id, patch) {
      const sets: string[] = []
      const params: SqlValue[] = []
      if (patch.url !== undefined) {
        params.push(patch.url)
        sets.push(`url = $${params.length}`)
      }
      if (patch.events !== undefined) {
        params.push(JSON.stringify(patch.events))
        sets.push(`events = $${params.length}::jsonb`)
      }
      if (patch.module !== undefined) {
        params.push(patch.module)
        sets.push(`module = $${params.length}`)
      }
      if (patch.status !== undefined) {
        params.push(patch.status)
        sets.push(`status = $${params.length}`)
        if (patch.status === 'active') {
          // Re-enable resets the failure count ‚Äî module doc.
          sets.push('consecutive_failures = 0')
        }
      }
      if (sets.length === 0) {
        const rows = await db.query<WebhookEndpointRow>(
          `SELECT ${WEBHOOK_ENDPOINT_COLUMNS} FROM webhook_endpoints WHERE id = $1`,
          [id],
        )
        const row = rows[0]
        return row === undefined ? null : toStoredWebhookEndpoint(row)
      }
      params.push(id)
      const rows = await db.query<WebhookEndpointRow>(
        `UPDATE webhook_endpoints SET ${sets.join(', ')}, updated_at = now()
         WHERE id = $${params.length}
         RETURNING ${WEBHOOK_ENDPOINT_COLUMNS}`,
        params,
      )
      const row = rows[0]
      return row === undefined ? null : toStoredWebhookEndpoint(row)
    },

    async delete(id) {
      const rows = await db.query<{ id: string }>(
        'DELETE FROM webhook_endpoints WHERE id = $1 RETURNING id',
        [id],
      )
      return rows.length === 1
    },

    async getSecret(id) {
      const rows = await db.query<{ secret_ciphertext: Uint8Array }>(
        'SELECT secret_ciphertext FROM webhook_endpoints WHERE id = $1',
        [id],
      )
      const row = rows[0]
      return row === undefined ? null : decrypt(row.secret_ciphertext, encryptionKey)
    },

    async recordDeliveryFailure(id) {
      const rows = await db.query<WebhookEndpointRow>(
        `UPDATE webhook_endpoints
         SET consecutive_failures = consecutive_failures + 1,
             status = CASE
               WHEN status = 'active' AND consecutive_failures + 1 >= $2 THEN 'auto_disabled'
               ELSE status
             END,
             updated_at = now()
         WHERE id = $1
         RETURNING ${WEBHOOK_ENDPOINT_COLUMNS}`,
        [id, WEBHOOK_AUTO_DISABLE_THRESHOLD],
      )
      const row = rows[0]
      return row === undefined ? null : toStoredWebhookEndpoint(row)
    },

    async recordDeliverySuccess(id) {
      const rows = await db.query<WebhookEndpointRow>(
        `UPDATE webhook_endpoints
         SET consecutive_failures = 0, updated_at = now()
         WHERE id = $1
         RETURNING ${WEBHOOK_ENDPOINT_COLUMNS}`,
        [id],
      )
      const row = rows[0]
      return row === undefined ? null : toStoredWebhookEndpoint(row)
    },
  }
}
